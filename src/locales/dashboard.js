export async function createDashboardI18n(storageApi = globalThis.chrome?.storage?.local) {
  let locale = (await readStoredLocale(storageApi)) || detectDashboardLocale()

  return {
    get locale() { return locale },
    locales: LOCALES,
    setLocale(newLocale) {
      if (LOCALES[newLocale]) locale = newLocale
    },
    t(key, vars) {
      const str = LOCALES[locale]?.[key] ?? LOCALES.en[key] ?? key
      if (!vars) return str
      return str.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? `{${k}}`)
    },
  }
}

export function detectDashboardLocale(languages = globalThis.navigator?.languages || [globalThis.navigator?.language]) {
  const normalized = languages.filter(Boolean).map((language) => String(language).replace('_', '-'))
  const firstChinese = normalized.find((language) => language.toLowerCase().startsWith('zh'))
  if (!firstChinese) return 'en'

  const lower = firstChinese.toLowerCase()
  if (lower.includes('cn') || lower.includes('sg') || lower.includes('hans')) {
    return 'zh_CN'
  }

  return 'zh_TW'
}

export async function storeDashboardLocale(locale, storageApi = globalThis.chrome?.storage?.local) {
  if (!LOCALES[locale]) return
  await storageSet(storageApi, { [LOCALE_KEY]: locale })
}

async function readStoredLocale(storageApi) {
  const result = await storageGet(storageApi, LOCALE_KEY)
  const stored = result?.[LOCALE_KEY]
  return LOCALES[stored] ? stored : null
}
